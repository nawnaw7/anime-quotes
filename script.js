var quote = $(".quoteText");
var author = $(".quoteAuthor");
var anime = $(".anime");


$("#nextQuote").on("click", function() {
    //Random background color + icon color
    var randomCol = getRandomColor();
    $(".body, button").css("background-color", randomCol); 
    $("#icon").css("color", randomCol);
    
    renderHTML();
});



function renderHTML() {
    var randomQ = anime_quotes[Math.floor(Math.random() * anime_quotes.length)];
    quote.text(randomQ.qtext);
    author.text(randomQ.qauthor);
    anime.text(randomQ.qanime);
}

function getRandomColor() {
    var letters = '0123456789ABCDEF';
    var color = '#';
    for (var i = 0; i < 6; i++ ) {
        color += letters[Math.floor(Math.random() * 16)];
    }
    return color;
}
 

$("#twitter").on("click", function() {
  window.open("https://twitter.com/intent/tweet?text=" + encodeURIComponent($(".quoteText").text() + " " + $(".quoteAuthor").text() + " - " + $(".anime").text()));
});


















